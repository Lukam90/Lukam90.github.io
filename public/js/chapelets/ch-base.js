/* Raccourcis */

document.addEventListener("keydown", e => {
    if (e.key == "0")   goTo("#");
    if (e.key == "1")   goTo("#credo");
    if (e.key == "2")   goTo("#pater");
    if (e.key == "3")   goTo("#ave");
    if (e.key == "4")   goTo("#gloria");
    if (e.key == "5")   goTo("#fatima");
    if (e.key == "6")   goTo("#salve");
});